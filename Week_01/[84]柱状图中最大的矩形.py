class Solution:
    def largestRectangleArea(self, heights: List[int]) -> int:
        max_area = 0
        stack = []
        heights = [0] + heights + [0]
        for i in range(len(heights)):
            while stack and heights[stack[-1]] > heights[i]:
                height_index = stack.pop()
                max_area = max(max_area, (i - stack[-1] -1) * heights[height_index])
            stack.append(i)
        return max_area
